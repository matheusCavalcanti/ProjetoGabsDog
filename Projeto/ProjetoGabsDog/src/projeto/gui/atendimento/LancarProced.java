    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        Date data = new Date(System.currentTimeMillis());
            
        Animal animal = new AnimalDAO().findById(Integer.parseInt(lblId.getText()));
        
        Procedimento proced = new ProcedimentoDAO().findById(Integer.parseInt(lblIdProc.getText()));
        
        Atendimento atend = new Atendimento();
        
        atend.setAnimal(animal);
        atend.setProced(proced);
        atend.setQuantidade(Integer.parseInt(txtQtde.getText()));
        atend.setValor(proced.getValor());
        atend.setData(data);
        
        AtendimentoDAO atendDAO = new AtendimentoDAO();
        atendDAO.save(atend);
        
        AbaterEstoque();
        
        JOptionPane.showMessageDialog(null, "Procedimento lançado com Sucesso!", "Sucesso!", JOptionPane.INFORMATION_MESSAGE);
        LancarProced.this.dispose();
        
    }//GEN-LAST:event_btnAddActionPerformed

    public void AbaterEstoque() {
        Procedimento proc = new ProcedimentoDAO().findById(Integer.valueOf(lblIdProc.getText()));
        
        //abate no estoque dos medicamentos ou da loja
        if (proc.getId() == 3 || proc.getId() == 5) {
            Estoque estoque = new Estoque();
            int novaQtde = estoque.abaterEstoque(proc.getQuantidade(), Integer.parseInt(txtQtde.getText()));
            proc.setQuantidade(novaQtde);
            
            ProcedimentoDAO procedimento = new ProcedimentoDAO();
            procedimento.uptade(proc);
        }
    }
    
