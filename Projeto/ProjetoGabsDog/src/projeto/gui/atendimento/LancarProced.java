    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        List<TipoProced> tipoProced = new TipoProcedDAO().findAll();
        
        tipoProced.forEach((tp) -> {
            cbProced.addItem(tp.getDescricao());
        });
    }//GEN-LAST:event_formWindowOpened

    private void cbProcedActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbProcedActionPerformed
        DefaultTableModel model =(DefaultTableModel) tabelaProced.getModel();
        model.setNumRows(0);
        
        TipoProced tipo = new TipoProced();
        tipo.setId(cbProced.getSelectedIndex() +1);
        tipo.setDescricao(cbProced.getSelectedItem().toString());
        
        List<Procedimento> proced = new ProcedimentoDAO().findProc(tipo);
        
        proced.forEach((p) -> {
            model.addRow(new Object[] {String.valueOf(p.getId()), p.getDescricao(), p.getValor() });
        });
    }//GEN-LAST:event_cbProcedActionPerformed

    private void tabelaProcedMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelaProcedMouseClicked
        int x = tabelaProced.getSelectedRow();
        Object obj = tabelaProced.getValueAt(x, 0);
        String id = String.valueOf(obj);
        
        Object obj2 = tabelaProced.getValueAt(x, 1);
        String nome = String.valueOf(obj2);
        
        lblIdProc.setText(id);
        lblProced.setText(nome);
    }//GEN-LAST:event_tabelaProcedMouseClicked

