    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        TipoProced tipo = new TipoProced();
        tipo.setId(definirTipo());
        
        String Nome = txtNome.getText() + "%";
        
        List<Procedimento> proceds = new ProcedimentoDAO().findNome(Nome, tipo);
        
        DefaultTableModel model =(DefaultTableModel) tabelaProd.getModel();
        model.setNumRows(0);
        
        if (proceds.isEmpty()){
            JOptionPane.showMessageDialog(rootPane, "Produto nÃ£o encontrado!", "Erro", JOptionPane.ERROR_MESSAGE);
        } else {
            proceds.forEach((p) -> {
                model.addRow(new Object[] { p.getId(), p.getDescricao(), p.getTipo().getDescricao(), p.getQuantidade(), p.getValor()});
            });
        }       
      
        tabelaProd.setModel(model);
        tabelaProd.repaint();
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnListarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnListarActionPerformed
        TipoProced tipo = new TipoProced();
        tipo.setId(definirTipo());
        
        DefaultTableModel model =(DefaultTableModel) tabelaProd.getModel();
        model.setNumRows(0);
        
        List<Procedimento> proceds = new ProcedimentoDAO().findProc(tipo);
        proceds.forEach((p) -> {
                model.addRow(new Object[] { p.getId(), p.getDescricao(), p.getTipo().getDescricao(), p.getQuantidade(), p.getValor()});
            });
        
        tabelaProd.setModel(model);
        tabelaProd.repaint();
        
        txtNome.setText("");
    }//GEN-LAST:event_btnListarActionPerformed

    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
        String x = String.valueOf(0);
        if (lblId.getText().equals(x) || lblId.getText().isEmpty()){
            JOptionPane.showMessageDialog(rootPane, "Escolha um Produto!", "Selecione", JOptionPane.WARNING_MESSAGE);
        } else {
            if (enviaId == null) {
                enviaId = new AlterarProduto();
                enviaId.setVisible(true);
                enviaId.setLocationRelativeTo(null);
                enviaId.recebe(Integer.parseInt(lblId.getText()));
            } else {
                enviaId.setVisible(true);
                enviaId.setState(AlterarProduto.NORMAL);
                enviaId.recebe(Integer.parseInt(lblId.getText()));
            }
        }
        lblId.setText(String.valueOf(0));
        
        DefaultTableModel model =(DefaultTableModel) tabelaProd.getModel();
        model.setNumRows(0);
    }//GEN-LAST:event_btnAlterarActionPerformed

    private void tabelaProdMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelaProdMouseClicked
        int x = tabelaProd.getSelectedRow();
        Object obj = tabelaProd.getValueAt(x, 0);
        String prod = String.valueOf(obj);
        
        lblId.setText(prod);
    }//GEN-LAST:event_tabelaProdMouseClicked

    public int definirTipo() {
        if (rbMed.isSelected()){
            return 3;
        } else { return 5; }
    }
    
